#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	s1_len;
	size_t	s2_len;
	size_t	total_len;
	char	*join;

	if (!s1 || !s2)
		return (NULL);
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	total_len = s1_len + s2_len + 1;
	join = (char *)malloc(sizeof(char) * (total_len));
	if (!join)
		return (NULL);
	ft_strlcpy(join, s1, total_len);
	ft_strlcat(join, s2, total_len);
	return (join);
}
